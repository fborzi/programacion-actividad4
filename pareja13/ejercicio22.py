# Crear el diccionario con los datos iniciales
habitantes = {
    "Junín": 102023,
    "Rojas": 28654,
    "Pergamino": 80569
}

# 1️⃣ Informar la cantidad de habitantes que tiene Pergamino
print("Habitantes de Pergamino:", habitantes["Pergamino"])

# 2️⃣ Agregar a Lincoln con 42.036 habitantes
habitantes["Lincoln"] = 42036
print("Agregada Lincoln:", habitantes)

# 3️⃣ Eliminar a Junín
del habitantes["Junín"]
print("Eliminado Junín:", habitantes)

# 4️⃣ Incrementar los habitantes de Rojas en 1.000
habitantes["Rojas"] += 1000
print("Rojas incrementada:", habitantes)

# 5️⃣ Modificar la cantidad de habitantes de Pergamino por 91.399
habitantes["Pergamino"] = 91399
print("Pergamino modificado:", habitantes)

# Mostrar el diccionario final
print("\nDiccionario final de habitantes:", habitantes)
